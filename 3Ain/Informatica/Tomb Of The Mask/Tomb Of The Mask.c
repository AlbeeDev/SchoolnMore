bool checkwalls(int mappa[alt][lung],int dir,int pl[2]){
	for(size_t i=0;i<alt;i++){
		for(size_t j=0;j<lung;j++){
			if(dir==1){
				if(i==pl[0]-1 && j==pl[1]){
					if(mappa[i][j]==1){
						return false;
					}
				}
			}
			if(dir==2){
				if(i==pl[0]+1 && j==pl[1]){
					if(mappa[i][j]==1){
						return false;
					}
				}
			}
			if(dir==3){
				if(i==pl[0] && j==pl[1]-1){
					if(mappa[i][j]==1){
						return false;
					}
				}
			}
			if(dir==4){
				if(i==pl[0] && j==pl[1]+1){
					if(mappa[i][j]==1){
						return false;
					}
				}
			}
		}	
	}
	return true;
}
