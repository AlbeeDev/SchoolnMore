int move(void){
	bool valid=false;
	char dir;
	while(valid==false){
		dir=getch();
		if (kbhit()){
			dir=getch();
		}
		switch(dir){
			case 'w':
				valid=true;
				return 1;
				break;
			case 's':
				valid=true;
				return 2;
				break;
			case 'a':
				valid=true;
				return 3;
				break;
			case 'd':
				valid=true;
				return 4;
				break;
			default:
				break; 
		}
	}
	
}
