package it.corso.datatype;

public class Primitivi {
    public static void main(String[] args) {
        byte pByte = 127;
        System.out.println(pByte);
        char pChar = 'a';
        System.out.println(pChar);
        long pLong = 1234567890000000000L;
        System.out.println(pLong);
        float pFloat = 123.456f;
        System.out.println(pFloat);
        double pDouble = 123.456;
        System.out.println(pDouble);
        boolean pBoolean = true;
        System.out.println(pBoolean);

        final int NUMERO_COSTANTE = 123;
        System.out.println(NUMERO_COSTANTE);

        Integer int1 = 123;
        System.out.println(int1);
        Integer int2 = null;
    }
}
